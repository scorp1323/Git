Все, что находится в скобках, будет вычислено и приведено к типу buul.
Сразу после условия следует одна инструкция, которая будет выполнена, если условие верное.
<?

$a = 10;
$b = 5;

if($a > $b) 
  echo $a;//10
if($a<$b)
  echo $b;
?>
Или можно использовать конструкцию else
<?
$a = 10;
$b = 51;

if($a > $b) 
  echo $a;
else
  echo $b;//51

?>
Но если наши переменные равны, результат тоже будет 10.
<?
$a = 101;
$b = 11;

if($a > $b) 
  echo $a;//101
elseif($a ==$b)
    echo 'Переменные равны';
else
  echo $b;
?>
Важно:
 - после каждой инструкции if, elseif, else выполниться одна инструкция. А нам может понадобиться больше (например, напечатан тип переменной или доп. сообщение или вычисление). Для етого после конструкции пишем символы "{}" - все инструкции нах. в етих фиг. скобках будут выполнены одним блоком.

 Исп. "{}" является правилом хорошего тона- реком. исп. всегда!!!
 
