Если, напр. при исп. if($arr), массив $arr пустой, первое условие не выполнится, пустой массив будет преобразован в bool false, выполнитя второе условие else. Но, если массив $arr не пустой - if преобразует условие в true - выполнится первое условие.
<?
$arr = array();
if ($arr) {
  echo 'Массив $arr не пустой!';
} else {
  echo 'Массив $arr - пустой!';
}//Массив $arr - пустой!
?>
