Приведение типов - преобразование переменных одного типа в другой тип.

Допускаются следующие приведения типов:
1) (int) (integer) - приведение к integer
2) (bool) (boolean) - приведение к boolean
3) (float) (double) (real) - прив. к float 
4) (string) - приведение к string
5) (array) - приведение к array
6) (object) - приведение к object
7) (unset) - приведение к Null