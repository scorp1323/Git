<?
$arr = array('red', 'orange');
echo '<pre>';
print_r($arr);
echo '<br>';
// Array
// (
//     [0] => red
//     [1] => orange
// )

array_unshift($arr, 'blue', 'green' );
echo '<pre>';
print_r($arr);
// Array
// (
//     [0] => blue
//     [1] => green
//     [2] => red
//     [3] => orange
// )