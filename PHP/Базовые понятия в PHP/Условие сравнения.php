<?php

$weather = 'дождь'; // снег, дождь, ясно, облачно

if ($weather == 'снег') {
	echo 'Погода плохая, лучше никуда не ехать!';
}

	if ($weather == 'дождь') {
	echo 'Погода плохая, лучше никуда не ехать! Дождь ведь!!!';
}

	if ($weather == 'ясно') {
	echo 'Все круто, кэп, можно ехать!';
}

	if ($weather == 'облачно') {
	echo 'Ехать можно, но погода может ухудшиться!';
}

